package com.coderfamily.lamj.domain;

public class SystemMainInfo {
    private int companyTotal;
    private int userTotal;
    private int taskTotal;

    public int getCompanyTotal() {
        return companyTotal;
    }

    public void setCompanyTotal(int companyTotal) {
        this.companyTotal = companyTotal;
    }

    public int getUserTotal() {
        return userTotal;
    }

    public void setUserTotal(int userTotal) {
        this.userTotal = userTotal;
    }

    public int getTaskTotal() {
        return taskTotal;
    }

    public void setTaskTotal(int taskTotal) {
        this.taskTotal = taskTotal;
    }
}
